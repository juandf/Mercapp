package com.android.store.mercapp.Models;

public class Producto {
    private int prdCod;
    private String prdNom;
    private int prdValor;

    public int getPrdCod() {
        return prdCod;
    }

    public void setPrdCod(int prdCod) {
        this.prdCod = prdCod;
    }

    public String getPrdNom() {
        return prdNom;
    }

    public void setPrdNom(String prdNom) {
        this.prdNom = prdNom;
    }

    public int getPrdValor() {
        return prdValor;
    }

    public void setPrdValor(int prdValor) {
        this.prdValor = prdValor;
    }
}
